# OPERATORS IN PYTHON
# Following are some common operators in python:

# 1. Arithmetic operators: +, -, * , / etc.
a = 5
b = 8
c = a + b 
print(c)

# 2. Assignment operators: =, +=, -= etc.
a = 4-2 # assign 4-2 in a.
print(a)
b = 6
b += 3 # b me 3 jod ke fir b ki value print hongi.
print(b)

# 3. Comparison operators: ==, >, >=, <, = etc.
d = 5<4 
print(d)
g = 400<=400
print(g)
j = 5!=5
print(j)

# 4. Logical operators: and, or, not.
e = True or False
print(e)  # 'or'
r = True and False
print(r)  # 'and'
print(not (False))  # 'not'
